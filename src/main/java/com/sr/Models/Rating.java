package com.sr.Models;

public class Rating {

    private String IdUser;
    private String idMovie;
    private double rating;

    public Rating(String idUser, String idMovie, double rating){
        this.IdUser=idUser;
        this.idMovie=idMovie;
        this.rating=rating;
    }

    public String darIdUser()
    {
        return darIdUser();
    }

    public String darIdMovie(){
        return idMovie;
    }

    public double darRating(){
        return rating;
    }



}
